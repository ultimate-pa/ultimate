package de.uni_freiburg.informatik.ultimate.modelcheckerutils.absint;

import java.util.Collection;

/**
 * {@link IAbstractTransformer} describes a post or pre operator for an {@link IAbstractDomain}. It is used to compute
 * the abstract post or pre state given an old {@link IAbstractState} and a transition.
 *
 * @author Daniel Dietsch (dietsch@informatik.uni-freiburg.de)
 * @author Marius Greitschus (greitsch@informatik.uni-freiburg.de)
 *
 */
public interface IAbstractTransformer<STATE extends IAbstractState<STATE>, ACTION> {

	/**
	 * Compute the abstract post for an old STATE (that extends {@link IAbstractState}) and a given ACTION (transition).
	 *
	 * @param oldstate
	 *            The abstract state before execution of the ACTION.
	 * @param transition
	 *            The transition that is taken.
	 * @return A new list of STATEs that is the result of applying the abstract post.
	 */
	Collection<STATE> apply(STATE oldstate, ACTION transition);

}
