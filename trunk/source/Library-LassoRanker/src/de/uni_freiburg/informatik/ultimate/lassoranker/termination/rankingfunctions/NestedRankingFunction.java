package de.uni_freiburg.informatik.ultimate.lassoranker.termination.rankingfunctions;

import java.math.BigInteger;
import java.util.LinkedHashSet;
import java.util.Map;
import java.util.Set;

import de.uni_freiburg.informatik.ultimate.lassoranker.termination.AffineFunction;
import de.uni_freiburg.informatik.ultimate.lib.modelcheckerutils.cfg.variables.IProgramVar;
import de.uni_freiburg.informatik.ultimate.lib.modelcheckerutils.smt.SmtUtils;
import de.uni_freiburg.informatik.ultimate.logic.Rational;
import de.uni_freiburg.informatik.ultimate.logic.SMTLIBException;
import de.uni_freiburg.informatik.ultimate.logic.Script;
import de.uni_freiburg.informatik.ultimate.logic.Term;


/**
 * A nested ranking function as generated by the NestedTemplate
 * 
 * @author Matthias Heizmann
 */
public class NestedRankingFunction extends RankingFunction {
	private static final long serialVersionUID = 380153194719949843L;
	
	private final AffineFunction[] mRanking;
	public final int mFunctions;
	
	public NestedRankingFunction(final AffineFunction[] ranking) {
		mRanking = ranking;
		mFunctions = ranking.length;
		assert(mFunctions > 0);
	}
	
	@Override
	public String getName() {
		return mRanking.length + "-nested";
	}

	
	@Override
	public Set<IProgramVar> getVariables() {
		final Set<IProgramVar> vars = new LinkedHashSet<IProgramVar>();
		for (final AffineFunction af : mRanking) {
			vars.addAll(af.getVariables());
		}
		return vars;
	}
	
	public AffineFunction[] getComponents() {
		return mRanking;
	}
	
	@Override
	public String toString() {
		final StringBuilder sb = new StringBuilder();
		sb.append(mRanking.length);
		sb.append("-nested ranking function:\n");
		for (int i = 0; i < mFunctions; ++i) {
			sb.append("  f" + i);
			sb.append(" = ");
			sb.append(mRanking[i]);
			if (i < mFunctions - 1) {
				sb.append("\n");
			}
		}
		return sb.toString();
	}
	
	@Override
	public Term[] asLexTerm(final Script script) throws SMTLIBException {
		// similar to the multiphase ranking function this can be seen as a
		// ranking function that proceed through phases.
		BigInteger n = BigInteger.ZERO;
		Term phase = SmtUtils.constructIntValue(script, n);
		Term value = mRanking[mRanking.length - 1].asTerm(script);
		for (int i = mRanking.length - 2; i >= 0; --i) {
			n = n.add(BigInteger.ONE);
			final Term f_term = mRanking[i].asTerm(script);
			final Term cond = script.term(">", f_term,
					SmtUtils.constructIntValue(script, BigInteger.ZERO));
			phase = script.term("ite", cond, SmtUtils.constructIntValue(script, n), phase);
			value = script.term("ite", cond, f_term, value);
		}
		return new Term[] { phase, value };
	}
	
	@Override
	public Ordinal evaluate(final Map<IProgramVar, Rational> assignment) {
		throw new UnsupportedOperationException("not yet implemented");
	}
	
	@Override
	public Ordinal codomain() {
		// phases * omega
		return Ordinal.fromInteger(BigInteger.valueOf(mRanking.length)).mult(Ordinal.OMEGA);
	}
}
